const mongoose = require('mongoose');

const CreditDetailSchema = new mongoose.Schema({
  credit: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Credit',
    required: true
  },
  product: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Product',
    required: true
  },
  quantity: {
    type: Number,
    required: true
  },
  unit_price: {
    type: Number,
    required: true
  },
  total_price: {
    type: Number,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('CreditDetail', CreditDetailSchema);
